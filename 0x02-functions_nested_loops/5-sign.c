#include "main.h"

/**
 * print_sign - prints sign depending on return
 * @n: receives integer
 * Return: always 1 when n is greater than 0
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
